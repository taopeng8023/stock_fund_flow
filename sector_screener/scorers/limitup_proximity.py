"""
因子 #25: 涨停邻近惩罚 — 涨幅 6-8% 最优，8%+ 次日衰减

回测依据:
  涨幅 6-8%:   次日均 +0.85% ← 最佳区间
  涨幅 8-9.5%: 次日均 -0.44% ← 涨停邻近衰减
  涨幅 3-6%:   次日均 -0.14%

距离涨停越近，次日回调风险越大。本因子给 6-8% 区间的股票加分，
给 8%+ 接近涨停的股票扣分。
"""
from sector_screener.config import to_float


def score_limitup_proximity(stock, context):
    """返回 0~1

    最优区间 6-8% → 得分 0.8+
    涨停邻近 8-9.5% → 得分 0.3-0.5（惩罚）
    涨停 ≥9.5% → 已被过滤，不进入评分
    低涨幅 <3% → 中性 0.5
    """
    f3 = to_float(stock.get("f3"))

    if f3 >= 9.5:
        return 0.1  # 涨停已被过滤，此分支理论上不会进入
    if f3 >= 8.0:
        # 涨停邻近: 8% → 0.5, 9.5% → 0.2 (线性衰减)
        return round(max(0.2, 0.5 - (f3 - 8.0) * 0.2), 3)
    if f3 >= 6.0:
        # 最优区间: 6% → 0.7, 8% → 0.85 (峰值在 7%)
        return round(0.7 + (f3 - 6.0) * 0.075, 3)
    if f3 >= 3.0:
        # 中等涨幅: 3% → 0.5, 6% → 0.7
        return round(0.5 + (f3 - 3.0) * 0.067, 3)
    if f3 >= 0:
        # 微涨: 0% → 0.4, 3% → 0.5
        return round(0.4 + f3 * 0.033, 3)

    # 下跌: 0% → 0.4, -5% → 0.3
    return round(max(0.3, 0.4 + f3 * 0.02), 3)
